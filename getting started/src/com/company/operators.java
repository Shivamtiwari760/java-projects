package com.company;

public class operators {
    public static void main(String[] args) {
        //arithmetic operators
        int a=7;
        int b=8+a;
        System.out.println(b);
        //assignmemnt operators
        int c=6;
        c*=9;
        System.out.println(c);
        //logical operators
        System.out.println(64>8 && 66>0);
        // bitwise operators
        System.out.println(2&3);
    }
}
